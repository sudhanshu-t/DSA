#include <bits/stdc++.h>
using namespace std;

class Pair{
public:
    int i;
    int j;
    Pair(int i, int j){
        this->i = i;
        this->j = j;
    }
};

unordered_set<string> ss;

string getAns(vector<vector<int>>& grid, int i, int j){
    queue<Pair> q;
    string s;
    int count = 0;
    q.push(Pair (i, j));
    
    while(!q.empty()){
        Pair f = q.front(); q.pop();
        
        if(grid[f.i][f.j] == 1){
            grid[f.i][f.j] = 2;
            count++;
        } else {
            continue;
        }
        
        if(f.i-1 >= 0 && grid[f.i-1][f.j] == 1){
            q.push(Pair(f.i-1, f.j));
            s += to_string(count) + "T";
        }
        
        if(f.j-1 >= 0 && grid[f.i][f.j-1] == 1){
            q.push(Pair(f.i, f.j-1));
            s += to_string(count) + "L";
        }
        
        if(f.i+1 < grid.size() && grid[f.i+1][f.j] == 1){
            q.push(Pair(f.i+1, f.j));
            s += to_string(count) + "D";
        }
        
        if(f.j+1 < grid[0].size() && grid[f.i][f.j+1] == 1){
            q.push(Pair(f.i, f.j+1));
            s += to_string(count) + "R";
        }

        // cout << s << endl;
        cout << f.i << " " << f.j << endl;
        
    }

    return s;
}

int numDistinctIslands(vector<vector<int>>& grid) {
    int count = 0;
    
    for(int i =0; i < grid.size(); i++){
        for(int j = 0; j < grid[0].size(); j++){
            if(grid[i][j] == 1){
                string s = getAns(grid, i, j);
                if(ss.find(s) == ss.end()){
                    count++;
                    ss.insert(s);

                    // cout << s << endl;
                }
            }
        }
    }
    
    return count;
}

